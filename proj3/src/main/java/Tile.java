public class Tile {

    double lrlon;
    double ullon;

    double lrlat;
    double ullat;

    double width;
    double height;

    public Tile(double lrlon, double ullon, double lrlat,
                double ullat, double width, double height) {

        this.lrlon = lrlon;
        this.ullon = ullon;
        this.lrlat = lrlat;
        this.ullat = ullat;
        this.width = width;
        this.height = height;

    }

}
